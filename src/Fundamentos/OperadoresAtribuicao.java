package Fundamentos;

public class OperadoresAtribuicao {
	public static void main(String[] args) {
		int a = 7;
		int b = 3;
		
		b += a;//significa que a sera somado a variavel b
		b *= 4;//significa que 4 sera multiplicado a variavel b
		b /= 2;//significa que 2 sera dividido a variavel b
		b -= 2;//significa que 2 sera subtraido da variavel b
		
		System.out.println(b);
		
	}
	

}
