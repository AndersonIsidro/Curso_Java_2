package Fundamentos;

public class ConversaoNumerica {
	public static void main(String[] args) {
		//Conversao explicita 1
		float f = (float)0.1;
		System.out.println(f);
		//conversao explicita 2
		int i1 = (int)7.9;
		System.out.println(i1);
		
		
		
		//conversao implicita1
		int i2 = 'a';
		System.out.println(i2);
		//conversao implicita2
		double d = 1001;
		System.out.println(d);
		
				
	}

}
