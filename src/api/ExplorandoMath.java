package api;

public class ExplorandoMath {
	public static void main(String[] args) {
		//metodos math
		System.out.println(Math.PI);//mostra o pi
		System.out.println(Math.pow(2, 3));//2 elevado a 3
		System.out.println(Math.abs(-10.5));//retorna valor absoluto
		System.out.println(Math.min(70, 7));//testa qual valor é menor e o retorna no console
		System.out.println(Math.max(70, 7));//testa qual valor é maior e o retorna no console
		System.out.println(Math.ceil(6.55));//arredonda o valor para cima e retorna
		System.out.println(Math.floor(6.55));//arredonda o valor para baixo e retorna
		System.out.println(Math.random());// mostra um valor entre 0 e 1 aleatorio com decimais
		System.out.println(Math.addExact(158, 3));//soma os numeros
	
	
	
	}

}
